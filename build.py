#!/usr/bin/env python3
"""
build.py — single-source site/CV generator for Ghulam Murtaza's webpage.

Reads:
    data/publications.bib   (papers; the only place you add publications)
    data/talks.yaml         (talks + posters)
    data/teaching.yaml      (courses)
    data/mentoring.yaml     (student advising)
    data/about.yaml         (landing page content)

Writes:
    cv/generated/*.tex      (\\input into cv/cv.tex — preserves the res.cls look)
    site/*.html             (the website pages)

No third-party deps except PyYAML. The BibTeX parser is a small self-contained
one (below) so there's nothing else to install in CI.
"""

import datetime
import html
import os
import re

import yaml

# Build/deploy time, shown in the site footer and at the end of the CV.
_BUILD_DT = datetime.datetime.now(datetime.timezone.utc)
BUILD_STAMP = f"{_BUILD_DT:%B} {_BUILD_DT.day}, {_BUILD_DT:%Y} at {_BUILD_DT:%H:%M} UTC"

ROOT = os.path.dirname(os.path.abspath(__file__))
DATA = os.path.join(ROOT, "data")
SITE = os.path.join(ROOT, "site")
GEN = os.path.join(ROOT, "cv", "generated")

# Custom .bib fields used only by this script (never shown in [BibTeX]).
PRIVATE_FIELDS = {"pubtype", "venue", "status", "award", "pdf", "code", "arxiv", "slides"}

GEN_HEADER = "% AUTO-GENERATED by build.py — do not edit. Edit data/ instead.\n"

# Order in which standard fields are emitted in the displayed BibTeX block.
BIBTEX_FIELD_ORDER = [
    "title", "author", "journal", "booktitle", "year", "volume",
    "number", "pages", "publisher", "doi", "url",
]


# --------------------------------------------------------------------------- #
#  Minimal BibTeX parser
# --------------------------------------------------------------------------- #
def parse_bib(text):
    """Parse a .bib string into a list of {type, key, fields, order}."""
    entries = []
    i, n = 0, len(text)
    while True:
        at = text.find("@", i)
        if at == -1:
            break
        brace = text.find("{", at)
        if brace == -1:
            break
        etype = text[at + 1:brace].strip().lower()
        # Read the balanced { ... } body.
        depth, j = 0, brace
        while j < n:
            c = text[j]
            if c == "{":
                depth += 1
            elif c == "}":
                depth -= 1
                if depth == 0:
                    break
            j += 1
        body = text[brace + 1:j]
        i = j + 1
        if etype in ("comment", "preamble", "string"):
            continue

        comma = body.find(",")
        if comma == -1:
            continue
        key = body[:comma].strip()
        rest = body[comma + 1:]

        order = []
        fields = {}
        p, m = 0, len(rest)
        while p < m:
            while p < m and rest[p] in " \t\r\n,":
                p += 1
            if p >= m:
                break
            eq = rest.find("=", p)
            if eq == -1:
                break
            fname = rest[p:eq].strip().lower()
            q = eq + 1
            while q < m and rest[q] in " \t\r\n":
                q += 1
            if q < m and rest[q] == "{":
                d, r = 0, q
                while r < m:
                    if rest[r] == "{":
                        d += 1
                    elif rest[r] == "}":
                        d -= 1
                        if d == 0:
                            break
                    r += 1
                val = rest[q + 1:r]
                p = r + 1
            elif q < m and rest[q] == '"':
                r = q + 1
                while r < m and rest[r] != '"':
                    r += 1
                val = rest[q + 1:r]
                p = r + 1
            else:
                r = q
                while r < m and rest[r] not in ",\n":
                    r += 1
                val = rest[q:r].strip()
                p = r
            val = " ".join(val.split())
            if fname:
                fields[fname] = val
                order.append(fname)
        entries.append({"type": etype, "key": key, "fields": fields, "order": order})
    return entries


# --------------------------------------------------------------------------- #
#  Author name helpers
# --------------------------------------------------------------------------- #
def split_authors(s):
    return [a.strip() for a in re.split(r"\s+and\s+", s) if a.strip()]


def name_parts(author):
    """Return (last_name, [given_tokens]) from a 'Last, First M' or 'First Last' string."""
    if "," in author:
        last, firsts = author.split(",", 1)
        last, firsts = last.strip(), firsts.strip()
    else:
        toks = author.split()
        last, firsts = (toks[-1] if toks else author), " ".join(toks[:-1])
    given = [t for t in re.split(r"[\s.]+", firsts) if t]
    return last, given


def is_me(last):
    return last.lower() == "murtaza"


# Lists longer than this are truncated to "first 3, ..., <me>, et al." so giant
# consortium papers don't blow up the CV/site. The [BibTeX] block always keeps
# the full list.
MAX_AUTHORS = 20


def _one_cv(author):
    last, given = name_parts(author)
    initials = "".join(t[0].upper() for t in given)
    name = (last + " " + initials).strip()
    return r"\textbf{" + name + "}" if is_me(last) else name


def _one_web(author):
    last, given = name_parts(author)
    # If a given token is a single letter, render it as an initial with a dot.
    rendered = " ".join((t if len(t) > 1 else t + ".") for t in given)
    full = h((rendered + " " + last).strip())
    return f"<strong>{full}</strong>" if is_me(last) else full


def _render_authors(author_field, render_one, ellipsis):
    authors = split_authors(author_field)
    rendered = [render_one(a) for a in authors]
    if len(rendered) <= MAX_AUTHORS:
        return ", ".join(rendered)
    me_idx = next((i for i, a in enumerate(authors) if is_me(name_parts(a)[0])), None)
    head = rendered[:3]
    if me_idx is not None and me_idx >= 3:
        return ", ".join(head) + f", {ellipsis} " + rendered[me_idx] + ", et al."
    return ", ".join(head) + ", et al."


def authors_cv(author_field):
    """CV style: 'Fang T, Wang X, \\textbf{Murtaza G}, ...'."""
    return _render_authors(author_field, _one_cv, r"\ldots")


def authors_web(author_field):
    """Website style: 'Tangqi Fang, ..., <strong>Ghulam Murtaza</strong>, ...' (HTML)."""
    return _render_authors(author_field, _one_web, "&hellip;")


# --------------------------------------------------------------------------- #
#  Escaping
# --------------------------------------------------------------------------- #
_LATEX_ESCAPES = {"&": r"\&", "%": r"\%", "#": r"\#", "_": r"\_"}


def tex_escape(s):
    return "".join(_LATEX_ESCAPES.get(c, c) for c in s)


# LaTeX accent command -> combining Unicode codepoint, for HTML rendering.
_ACCENTS = {
    '"': "̈", "'": "́", "`": "̀", "^": "̂",
    "~": "̃", "=": "̄", ".": "̇", "v": "̌",
    "c": "̧", "u": "̆", "H": "̋",
}
_ACCENT_RE = re.compile(r'\{?\\(?P<acc>["\'`^~=.vcuH])\{?(?P<ch>[A-Za-z])\}?\}?')


def delatex(s):
    """Turn LaTeX accents like {\\"u} into real Unicode (ü) and drop stray braces."""
    def repl(m):
        comb = _ACCENTS.get(m.group("acc"))
        ch = m.group("ch")
        return __import__("unicodedata").normalize("NFC", ch + comb) if comb else ch
    s = _ACCENT_RE.sub(repl, s)
    return s.replace("{", "").replace("}", "")


def h(s):
    """Escape for HTML display, converting LaTeX accents to Unicode."""
    return html.escape(delatex(s), quote=True)


def h_raw(s):
    """Escape for HTML without touching LaTeX (used for the copy-paste BibTeX)."""
    return html.escape(s, quote=True)


# --------------------------------------------------------------------------- #
#  Publications
