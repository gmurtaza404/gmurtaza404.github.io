def load_publications():
    with open(os.path.join(DATA, "publications.bib"), encoding="utf-8") as f:
        entries = parse_bib(f.read())
    for e in entries:
        e["year"] = int(re.sub(r"\D", "", e["fields"].get("year", "0")) or 0)
        e["pubtype"] = e["fields"].get("pubtype", "journal").lower()
    return entries


def cv_pub_line(e):
    """One CV paragraph for a publication, matching the original styling."""
    f = e["fields"]
    authors = authors_cv(f.get("author", ""))
    title = tex_escape(f.get("title", ""))
    if e["pubtype"] == "preprint" and f.get("status"):
        italic = tex_escape(f["status"])
    else:
        italic = tex_escape(f.get("venue", f.get("journal", f.get("booktitle", ""))))
    asep = "" if authors.endswith(".") else "."
    line = f"{authors}{asep} {title}. \\textit{{{italic}}}."
    if f.get("award"):
        line += f" ({tex_escape(f['award'])})"
    return line


def clean_bibtex(e):
    """Re-emit a tidy BibTeX entry with private fields stripped (for the website)."""
    fields = {k: v for k, v in e["fields"].items() if k not in PRIVATE_FIELDS}
    ordered = [k for k in BIBTEX_FIELD_ORDER if k in fields]
    ordered += [k for k in fields if k not in ordered]
    lines = [f"@{e['type']}{{{e['key']},"]
    for k in ordered:
        lines.append(f"  {k:<9}= {{{fields[k]}}},")
    if len(lines) > 1:
        lines[-1] = lines[-1].rstrip(",")
    lines.append("}")
    return "\n".join(lines)


def web_pub_item(e):
    """One <li> for the publications page, with a [BibTeX] <details> dropdown."""
    f = e["fields"]
    authors = authors_web(f.get("author", ""))
    title = h(f.get("title", ""))
    if e["pubtype"] == "preprint":
        venue = f.get("venue") or f.get("status") or f.get("journal", "")
    else:
        venue = f.get("venue") or f.get("journal") or f.get("booktitle", "")
    venue = h(venue)

    links = []
    for label, field in (("PDF", "pdf"), ("Code", "code"),
                         ("arXiv", "arxiv"), ("Slides", "slides")):
        if f.get(field):
            links.append(f'<a href="{h(f[field])}">[{label}]</a>')
    bibtex = h_raw(clean_bibtex(e))
    links.append(
        '<details class="bibtex"><summary></summary>'
        f'<pre>{bibtex}</pre></details>'
    )

    award = f' <span class="award">({h(f["award"])})</span>' if f.get("award") else ""
    meta = f'<span class="venue">{venue}</span>' if venue else ""
    asep = "" if authors.endswith(".") else "."
    return (
        '  <li class="pub">\n'
        f'    <span class="authors">{authors}{asep}</span> '
        f'<span class="title">{title}.</span> {meta}{award}\n'
        f'    <div class="pub-links">{" ".join(links)}</div>\n'
        '  </li>'
    )


