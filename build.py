# --------------------------------------------------------------------------- #
#  CV fragment generation
# --------------------------------------------------------------------------- #
def write_cv_fragments(pubs, talks, teaching, mentoring):
    os.makedirs(GEN, exist_ok=True)

    def pubs_of(t):
        items = sorted([e for e in pubs if e["pubtype"] == t],
                       key=lambda e: -e["year"])
        return "\n\n".join(cv_pub_line(e) for e in items) + "\n"

    fragments = {
        "preprints.tex": pubs_of("preprint"),
        "conference.tex": pubs_of("conference"),
        "journal.tex": pubs_of("journal"),
        "workshop.tex": pubs_of("workshop"),
    }

    # Posters (and any talks) for the CV.
    def talk_block(kind):
        items = [t for t in talks if t.get("type") == kind]
        items.sort(key=lambda t: str(t.get("date", "")), reverse=True)
        return "\n\n".join(
            f"{tex_escape(t['title'])} \\textit{{{tex_escape(t['venue'])}}}"
            for t in items
        ) + "\n"

    fragments["posters.tex"] = talk_block("poster")
    fragments["talks.tex"] = talk_block("talk")

    # "Last updated" line pinned to the bottom-center of page 1 (eso-pic).
    fragments["updated.tex"] = (
        r"\AddToShipoutPictureBG*{%" + "\n"
        r"  \AtPageLowerLeft{%" + "\n"
        r"    \put(\LenToUnit{0.5\paperwidth},\LenToUnit{0.4in}){%" + "\n"
        r"      \makebox(0,0){\footnotesize\textit{Last updated: "
        + BUILD_STAMP + r"}}}}}" + "\n"
    )

    # Teaching courses.
    tlines = []
    for c in teaching:
        s = f"{c['role']} for {c['course']} ({c['term']}), {c['institution']}"
        if c.get("enrollment"):
            s += f" (Enrollment: {c['enrollment']} students)"
        tlines.append(tex_escape(s) + r" \\")
    fragments["teaching.tex"] = "\n".join(tlines) + "\n"

    # Student advising.
    blocks = []
    for m in mentoring:
        b = [
            r"\textbf{" + tex_escape(m["name"]) + r"} \\",
            tex_escape(f"Project: {m['project']}") + r" \\",
            tex_escape(m["lab"]),
        ]
        blocks.append("\n".join(b))
    fragments["advising.tex"] = "\n\n".join(blocks) + "\n"

    for fname, content in fragments.items():
        with open(os.path.join(GEN, fname), "w", encoding="utf-8") as fp:
            fp.write(GEN_HEADER + content)


def write_cv_only_fragments(service, honors, references):
    """CV-only sections (not on the website): service, honors, references."""
    def line_list(items):
        return "\n".join(tex_escape(x) + r" \\" for x in items) + "\n"

    ref_blocks = []
    for r in references:
        head = r"\textbf{" + tex_escape(r["name"]) + "}"
        if r.get("relation"):
            head += f" ({tex_escape(r['relation'])})"
        block = [
            head + r" \\",
            r"\textbf{Appointment:} " + tex_escape(r["appointment"]) + r" \\",
            r"\textbf{Email:} " + tex_escape(r["email"]),
        ]
        ref_blocks.append("\n".join(block))

    fragments = {
        "service_department.tex": line_list(service.get("department", [])),
        "service_profession.tex": line_list(service.get("profession", [])),
        "honors.tex": line_list(honors),
        "references.tex": "\n\n".join(ref_blocks) + "\n",
    }
    for fname, content in fragments.items():
        with open(os.path.join(GEN, fname), "w", encoding="utf-8") as fp:
            fp.write(GEN_HEADER + content)


# --------------------------------------------------------------------------- #
#  HTML site generation
# --------------------------------------------------------------------------- #
NAV = [
    ("index.html", "About"),
    ("publications.html", "Publications"),
    ("talks.html", "Talks &amp; Posters"),
    ("teaching.html", "Teaching &amp; Mentoring"),
    ("cv.html", "CV"),
]


def page(active, title, body, name):
    nav = "\n".join(
        f'      <a href="{href}"{" class=\"active\"" if href == active else ""}>{label}</a>'
        for href, label in NAV
    )
    return f"""<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="utf-8">
  <meta name="viewport" content="width=device-width, initial-scale=1">
  <title>{h(name)} — {title}</title>
  <link rel="stylesheet" href="style.css">
</head>
<body>
  <header class="site-header">
    <a class="site-name" href="index.html">{h(name)}</a>
    <nav class="site-nav">
{nav}
    </nav>
  </header>
  <main>
{body}
  </main>
  <footer class="site-footer">
    <p>Last updated {BUILD_STAMP}. &copy; {h(name)}.</p>
  </footer>
</body>
</html>
"""


def build_index(about):
    name = about.get("name", "")
    photo = about.get("photo") or ""
    photo_html = f'<img class="avatar" src="{h(photo)}" alt="{h(name)}">' if photo else ""
    bio = "\n".join(f"    <p>{h(p)}</p>" for p in about.get("bio", []))
    links = []
    for label, url in (about.get("links") or {}).items():
        if url:
            links.append(f'<a href="{h(url)}">{h(label)}</a>')
    links_html = (
        f'    <div class="links">{" · ".join(links)}</div>' if links else ""
    )
    interests = about.get("interests", "")
    interests_html = f'    <p class="interests">{h(interests)}</p>' if interests else ""
    body = f"""    <section class="hero">
      {photo_html}
      <div class="hero-text">
        <h1>{h(name)}</h1>
        <p class="tagline">{h(about.get("tagline", ""))}</p>
{interests_html}
{links_html}
      </div>
    </section>
    <section class="bio">
{bio}
    </section>"""
    return page("index.html", "About", body, name)


def build_publications(pubs, name):
    def section(label, items):
        if not items:
            return ""
        lis = "\n".join(web_pub_item(e) for e in items)
        return f'    <h2>{label}</h2>\n    <ul class="pub-list">\n{lis}\n    </ul>'

    preprints = sorted([e for e in pubs if e["pubtype"] == "preprint"],
                       key=lambda e: -e["year"])
    published = sorted([e for e in pubs if e["pubtype"] != "preprint"],
                       key=lambda e: -e["year"])
    body = "    <h1>Publications</h1>\n"
    body += section("Preprints", preprints) + "\n"
    body += section("Peer-reviewed", published)
    return page("publications.html", "Publications", body, name)


def build_talks(talks, name):
    items = sorted(talks, key=lambda t: str(t.get("date", "")), reverse=True)
    rows = []
    for t in items:
        tag = t.get("type", "talk").capitalize()
        loc = f' · {h(t["location"])}' if t.get("location") else ""
        links = []
        for label, field in (("Slides", "slides"), ("Poster", "poster"),
                             ("Video", "video")):
            url = (t.get("links") or {}).get(field)
            if url:
                links.append(f'<a href="{h(url)}">[{label}]</a>')
        links_html = f' <span class="pub-links">{" ".join(links)}</span>' if links else ""
        rows.append(
            '  <li class="talk">\n'
            f'    <span class="tag tag-{t.get("type", "talk")}">{tag}</span>\n'
            f'    <span class="title">{h(t["title"])}</span>\n'
            f'    <div class="venue">{h(t["venue"])}{loc}{links_html}</div>\n'
            '  </li>'
        )
    body = ('    <h1>Talks &amp; Posters</h1>\n'
            f'    <ul class="talk-list">\n' + "\n".join(rows) + "\n    </ul>")
    return page("talks.html", "Talks & Posters", body, name)


def build_teaching(teaching, mentoring, name):
    courses = []
    for c in teaching:
        enr = f' · Enrollment: {c["enrollment"]} students' if c.get("enrollment") else ""
        courses.append(
            '  <li class="course">\n'
            f'    <span class="title">{h(c["course"])}</span>\n'
            f'    <div class="venue">{h(c["role"])} · {h(c["institution"])} · '
            f'{h(c["term"])}{enr}</div>\n'
            '  </li>'
        )
    mentees = []
    for m in mentoring:
        mentees.append(
            '  <li class="mentee">\n'
            f'    <span class="title">{h(m["name"])}</span>'
            f' <span class="affil">{h(m["lab"])}</span>\n'
            f'    <div class="venue">{h(m["project"])}</div>\n'
            '  </li>'
        )
    body = (
        '    <h1>Teaching &amp; Mentoring</h1>\n'
        '    <h2>Courses</h2>\n'
        '    <ul class="course-list">\n' + "\n".join(courses) + "\n    </ul>\n"
        '    <h2>Student Advising</h2>\n'
        '    <ul class="mentee-list">\n' + "\n".join(mentees) + "\n    </ul>"
    )
    return page("teaching.html", "Teaching & Mentoring", body, name)


def build_cv_page(name):
    body = """    <h1>Curriculum Vitae</h1>
    <p><a class="button" href="cv.pdf" download>Download PDF</a></p>
    <object class="cv-embed" data="cv.pdf" type="application/pdf">
      <p>Your browser can't display the embedded PDF.
         <a href="cv.pdf">Download the CV here</a>.</p>
    </object>"""
    return page("cv.html", "CV", body, name)


# --------------------------------------------------------------------------- #
#  Main
# --------------------------------------------------------------------------- #
def main():
    pubs = load_publications()
    talks = load_yaml("talks.yaml")
    teaching = load_yaml("teaching.yaml")
    mentoring = load_yaml("mentoring.yaml")
    service = load_yaml("service.yaml") or {}
    honors = load_yaml("honors.yaml")
    references = load_yaml("references.yaml")
    with open(os.path.join(DATA, "about.yaml"), encoding="utf-8") as f:
        about = yaml.safe_load(f) or {}
    name = about.get("name", "")

    write_cv_fragments(pubs, talks, teaching, mentoring)
    write_cv_only_fragments(service, honors, references)

    os.makedirs(SITE, exist_ok=True)
    pages = {
        "index.html": build_index(about),
        "publications.html": build_publications(pubs, name),
        "talks.html": build_talks(talks, name),
        "teaching.html": build_teaching(teaching, mentoring, name),
        "cv.html": build_cv_page(name),
    }
    for fname, content in pages.items():
        with open(os.path.join(SITE, fname), "w", encoding="utf-8") as fp:
            fp.write(content)

    print(f"Built {len(pubs)} publications, {len(talks)} talks/posters, "
          f"{len(teaching)} courses, {len(mentoring)} mentees.")
    print(f"  -> cv/generated/*.tex")
    print(f"  -> site/*.html")


if __name__ == "__main__":
    main()
